largura = float(input("Largura da parede"))
altura = float(input("Altura da parede"))
area = largura * altura
print("Sua parede tem a dimensão de {}x{} e sua área é de {}metros quadrados".format(largura,altura,largura * altura))
tinta = area / 2
print("Para pinta essa parede voce precisa de {} litros de tinta ".format(tinta))
